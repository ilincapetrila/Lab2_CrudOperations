public class Books {
    private String name;
    private String author;
    private String type;
    public String getName() {
        return name;
    }
    public String getAuthor() {
        return author;
    }
    public String getType() {
        return type;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setAuthor(String author) {
        this.author = author;
    }
    public void setType(String type){
        this.type=type;
    }

}
